<?php

namespace App\Http\Livewire;

use App\Domains\TimeZone\Models\Timezone;
use Illuminate\Database\Eloquent\Builder;
use Rappasoft\LaravelLivewireTables\TableComponent;
use Rappasoft\LaravelLivewireTables\Views\Column;

/**
 * Class TimezoneTable.
 */
class TimezoneTable extends TableComponent
{
    /**
     * @var string
     */
    public $sortField = 'id';

    /**
     * @var string
     */
    public $status;

    /**
     * @param string $status
     */
    public function mount($status = null): void
    {
        $this->status = $status;
    }

    /**
     * @return Builder
     */
    public function query(): Builder
    {
        $query = Timezone::query();

        if ($this->status === 'deleted') {
            return $query->onlyTrashed();
        }

        return $query;
    }

    /**
     * @return array
     */
    public function columns(): array
    {
        return [
            Column::make(__('id'), 'id')
,
            Column::make(__('zone'), 'zone')
,

            Column::make(__('Actions'))
                ->format(function (Timezone $model) {
                    return view('backend.time-zone.includes.actions',  ['timezone' => $model]);
                })
        ];
    }
}